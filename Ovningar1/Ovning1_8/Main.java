class Main {
  public static void main(String[] args) {
    double tal1 = 3.5;
    double tal2 = 1.5;
    
    System.out.println((tal1 + tal2) + " = " + tal1 + " + " + tal2);

    System.out.println((tal1 - tal2) + " = " + tal1 + " - " + tal2);

    System.out.println((tal1 * tal2) + " = " + tal1 + " * " + tal2);

    System.out.println((tal1 / tal2) + " = " + tal1 + " / " + tal2);
  }
}